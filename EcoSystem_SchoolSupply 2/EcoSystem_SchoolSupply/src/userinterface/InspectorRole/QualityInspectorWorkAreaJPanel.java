    public void populateDetails(){
     
      for (int i=0; i<result.size();i++) {
        InspectionWorkRequest labTestWorkRequest = (InspectionWorkRequest) result.get(i);
        SchoolPackage schoolPackage =  labTestWorkRequest.getSchoolPackage();
        if(schoolPackage.getStatus()=="Pending"){
        boardTotalJText.setText(String.valueOf(schoolPackage.getNumberOfBoards()));
        tableTotalJText.setText(String.valueOf(schoolPackage.getNumberOfTables()));
        notebookTotalJText.setText(String.valueOf(schoolPackage.getNumberOfNotebooks()));
        maskTotalJText.setText(String.valueOf(schoolPackage.getNumberOfMasks()));
        break;
        }
      }
    }
static boolean isNumberValid(String number)
    {
        String regex = "[\\\\s]*[0-9]*[0-9]+";
        return number.matches(regex);
    }
