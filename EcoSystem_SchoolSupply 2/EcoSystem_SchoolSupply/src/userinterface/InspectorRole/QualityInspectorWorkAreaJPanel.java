    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        boardApprovedJText.setBorder(new LineBorder(new Color(128,128,128)));
        jLabel2.setForeground(Color.BLACK);

        tableApprovedJText.setBorder(new LineBorder(new Color(128,128,128)));
        jLabel3.setForeground(Color.BLACK);

        notebookApprovedJText.setBorder(new LineBorder(new Color(128,128,128)));
        jLabel4.setForeground(Color.BLACK);

        maskApprovedJText.setBorder(new LineBorder(new Color(128,128,128)));
        jLabel5.setForeground(Color.BLACK);

        if(isNumberValid(boardApprovedJText.getText()) == false || boardApprovedJText.getText() == null || boardApprovedJText.getText().equals(""))
        {
            boardApprovedJText.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel2.setForeground(Color.RED);
            JOptionPane.showMessageDialog(null, "Please enter a valid number for No. of Boards");
        }
        else if(isNumberValid(tableApprovedJText.getText()) == false || tableApprovedJText.getText() == null || tableApprovedJText.getText().equals(""))
        {
            tableApprovedJText.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel3.setForeground(Color.RED);
            JOptionPane.showMessageDialog(null, "Please enter a valid number for No. Of Tables");
        }
        else if(isNumberValid(notebookApprovedJText.getText()) == false || notebookApprovedJText.getText() == null || notebookApprovedJText.getText().equals(""))
        {
            notebookApprovedJText.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel4.setForeground(Color.RED);
            JOptionPane.showMessageDialog(null, "Please enter a valid number for No. Of NoteBooks");
        }
        else if(isNumberValid(maskApprovedJText.getText()) == false || maskApprovedJText.getText() == null || maskApprovedJText.getText().equals(""))
        {
            maskApprovedJText.setBorder(BorderFactory.createLineBorder(Color.RED));
            jLabel5.setForeground(Color.RED);
            JOptionPane.showMessageDialog(null, "Please enter a valid number for No. Of Masks");
        }
        else
        {
            ArrayList<WorkRequest> result = business.getWorkQueue().getWorkRequestList();
            for (int i=0; i<result.size();i++) {
                ManufacturingWorkRequest manufacturingworkrequest = (ManufacturingWorkRequest) result.get(i);

                int numberOfBoards = Integer.parseInt(boardApprovedJText.getText());
                int numberOfTables = Integer.parseInt(tableApprovedJText.getText());
                int numberOfNotebooks = Integer.parseInt(notebookApprovedJText.getText());
                int numberOfMasks = Integer.parseInt(maskApprovedJText.getText());

                int totalNumberOfBoards = Integer.parseInt(boardTotalJText.getText());
                int totalNumberOfTables = Integer.parseInt(tableTotalJText.getText());
                int totalNumberOfNotebooks = Integer.parseInt(notebookTotalJText.getText());
                int totalNumberOfMasks = Integer.parseInt(maskTotalJText.getText());

                if(totalNumberOfBoards<numberOfBoards){
                    JOptionPane.showMessageDialog(null, "Please enter quantity less than Total Quantity");
                    return;
                }
                if(totalNumberOfTables<numberOfTables){
                    JOptionPane.showMessageDialog(null, "Please enter quantity less than Total Quantity");
                    return;
                }
                if(totalNumberOfNotebooks<numberOfNotebooks){
                    JOptionPane.showMessageDialog(null, "Please enter quantity less than Total Quantity");
                    return;
                }
                if(totalNumberOfMasks<numberOfMasks){
                    JOptionPane.showMessageDialog(null, "Please enter quantity less than Total Quantity");
                    return;
                }
                 SchoolPackage schoolPackage = manufacturingworkrequest.getSchoolPackage();
            if(schoolPackage==null){
                schoolPackage = new SchoolPackage();
            }
            numberOfBoards = numberOfBoards+((String.valueOf(schoolPackage.getNumberOfBoards()))==null?0:schoolPackage.getNumberOfBoards());
            schoolPackage.setNumberOfBoards(numberOfBoards);
            
            numberOfTables = numberOfTables+((String.valueOf(schoolPackage.getNumberOfTables()))==null?0:schoolPackage.getNumberOfTables());
            schoolPackage.setNumberOfTables(numberOfTables);
            
            numberOfNotebooks = numberOfNotebooks+((String.valueOf(schoolPackage.getNumberOfNotebooks()))==null?0:schoolPackage.getNumberOfNotebooks());
            schoolPackage.setNumberOfNotebooks(numberOfNotebooks);
            
            numberOfMasks = numberOfMasks+((String.valueOf(schoolPackage.getNumberOfMasks()))==null?0:schoolPackage.getNumberOfMasks());
            schoolPackage.setNumberOfMasks(numberOfMasks);
            
            schoolPackage.setStatus("Completed");
            
            manufacturingworkrequest.setSchoolPackage(schoolPackage);
            JOptionPane.showMessageDialog(null, "School Package added Succesfully");
            request.setStatus("Completed");
         }
            

            boardApprovedJText.setText("");
            tableApprovedJText.setText("");
            notebookApprovedJText.setText("");
            maskApprovedJText.setText("");

            boardTotalJText.setText("");
            tableTotalJText.setText("");
            notebookTotalJText.setText("");
            maskTotalJText.setText("");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

